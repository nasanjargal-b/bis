package com.monsource.bis.blank.model;

public class Blanks {

	private String id;
	private String name;
	private String blankGroupName;

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBlankGroupName() {
		return this.blankGroupName;
	}

	public void setBlankGroupName(String blankGroupName) {
		this.blankGroupName = blankGroupName;
	}

}